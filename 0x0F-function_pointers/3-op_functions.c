#include "3-calc.h"
/**
 * op_add - adds two digits.
 * @a: digit 1.
 * @b: digit 2.
 * Return: the sum of a && b.
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracts two digits.
 * @a: digit 1.
 * @b: digit 2.
 * Return: the difference of a && b.
 */
int op_sub(int a, int b);
{
	return (a - b);
}
/**
 * op_mul - multiplies two digits.
 * @a: digit 1.
 * @b: digit 2.
 * Return: the product of a && b.
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides two digits.
 * @a: digit 1.
 * @b: digit 2.
 * Return: the result of division of a && b.
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - returns modulus of two digits.
 * @a: digit 1.
 * @b: digit 2.
 * Return: the remainder of division of a && b.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
